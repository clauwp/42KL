int	ft_atoi(char *str)
{
	int	isNeg;
	int	num;

	isNeg = 1;
	while (*str >= 9 && *str <=13 | *str == 32)
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		isNeg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = (num * 10) + (*str - '0');
		str++;
	}
	return (num * isNeg);
}